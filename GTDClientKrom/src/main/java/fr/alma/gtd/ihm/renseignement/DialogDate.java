	private void updateMatrice() {
		GregorianCalendar cal = new GregorianCalendar((Integer)(spinnerAnnee.getValue()), comboMois.getSelectedIndex(), 1);
		//cal.setFirstDayOfWeek(Calendar.MONDAY);
		//efface le tableau
		for (int i=0; i<6; i++) {
			for (int j=0; j<7; j++) {
				tableauJour.setValueAt(null, i, j);
			}
		}
		//remplie le tableau
		int i = 0;
		int start = cal.get(Calendar.DAY_OF_WEEK);
		while (i<cal.getActualMaximum(Calendar.DAY_OF_MONTH)) {
			tableauJour.setValueAt(i+1, (i+start-1)/7, (i+start-1)%7);
			i++;
		}
	}
	
	public Date showDialog() {
		dateResultat = null;
		this.setVisible(true);
		return dateResultat;
	}

