	private void tableauJourMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableauJourMouseClicked
		int ligne = tableauJour.rowAtPoint(evt.getPoint());
		int colonne = tableauJour.columnAtPoint(evt.getPoint());
		Integer jour = (Integer) tableauJour.getValueAt(ligne, colonne);
		if (jour != null) {
			GregorianCalendar calendrierResultat = new GregorianCalendar();
			calendrierResultat.set((Integer)spinnerAnnee.getValue(), comboMois.getSelectedIndex(), jour);
			dateResultat = calendrierResultat.getTime();
		}
		dispose();
	}//GEN-LAST:event_tableauJourMouseClicked

