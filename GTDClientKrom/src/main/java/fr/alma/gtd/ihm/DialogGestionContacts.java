	private void listContactsValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_listContactsValueChanged
		int indice = listContacts.getLeadSelectionIndex();
		if (indice >= 0) {
			Contact c = (Contact) listeDisponibleModel.get(indice);
			textNom.setText(c.getNom());
			textPrenom.setText(c.getPrenom());
			textAdresse.setText(c.getAdresse());
			textEMail.setText(c.getEmail());
			textTelephone.setText(c.getTelephone());
		} else {
			textNom.setText(null);
			textPrenom.setText(null);
			textAdresse.setText(null);
			textEMail.setText(null);
			textTelephone.setText(null);
		}
	}//GEN-LAST:event_listContactsValueChanged

	private void listContactsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listContactsMouseClicked
		if (evt.getButton() == java.awt.event.MouseEvent.BUTTON3) {
			int index = listContacts.locationToIndex(evt.getPoint());
			if (index != -1) {
				listContacts.setSelectedIndex(index);
				javax.swing.JPopupMenu jpm = new javax.swing.JPopupMenu();
				javax.swing.JMenuItem modifMenu = new javax.swing.JMenuItem("Modifier le contact");
				modifMenu.addActionListener(new ListModifMenuListener(listContacts.getSelectedValue()));
				jpm.add(modifMenu);
				javax.swing.JMenuItem eraseMenu = new javax.swing.JMenuItem("Effacer le contact");
				eraseMenu.addActionListener(new ListEraseMenuListener(listContacts.getSelectedValue()));
				jpm.add(eraseMenu);
				jpm.show(listContacts, evt.getX(), evt.getY());
			}
		}
	}//GEN-LAST:event_listContactsMouseClicked

	class ListEraseMenuListener implements java.awt.event.ActionListener{
		private Object object;
		public ListEraseMenuListener(Object obj){
			this.object = obj;
		}
		@Override
		public void actionPerformed(java.awt.event.ActionEvent e) {
			controleur.supprimerContact(object);
			listeDisponibleModel.removeElement(object);
		}
	}

	class ListModifMenuListener implements java.awt.event.ActionListener{
		private Contact contact;
		public ListModifMenuListener(Object obj){
			this.contact = (Contact) obj;
		}
		@Override
		public void actionPerformed(java.awt.event.ActionEvent e) {
			DialogAjoutContact dialog = new DialogAjoutContact(null, true);
			dialog.setInfos(contact);
			Contact temp = dialog.showDialog();
			if (temp != null) {
				contact.setNom(temp.getNom());
				contact.setPrenom(temp.getPrenom());
				contact.setAdresse(temp.getAdresse());
				contact.setEmail(temp.getEmail());
				contact.setTelephone(temp.getTelephone());
				controleur.actualiserContact(contact);
				listContactsValueChanged(null);
			}
		}
	}

