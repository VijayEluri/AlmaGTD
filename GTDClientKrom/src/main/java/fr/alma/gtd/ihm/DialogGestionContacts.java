	private Controleur controleur;

	private void initialisation() {
		controleur = Controleur.getInstance();
		List<Contact> listeContactsDisponible = controleur.getListeContacts();
		for (Contact c : listeContactsDisponible) {
			listeDisponibleModel.addElement(c);
		}
	}

